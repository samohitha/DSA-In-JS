// ---------------------------Do While Loops--------------------------------//

let y = 10;

while (y < 5) {
  y++;
}

console.log(y);

// The above statement never executes

// In do while irrespective of condition the loop execute at least once

do {
  y++;
} while (y < 12); // if this is true go back and execute again
{
  console.log(y);
}


